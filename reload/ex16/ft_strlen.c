int	ft_strlen(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (str[0] == 0)
	{
		return (0);
	}
	while (str[i] != 0)
	{
		i++;
		j++;
	}
	return (j);
}

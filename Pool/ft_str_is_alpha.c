int	ft_str_is_alpha(char *str)
{
	int	c;

	c = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[c] != '\0')
	{
		if (!(str[c] >= 'a' && str[c] <= 'z'))
		{
			if (!(str[c] >= 'A' && str[c] <= 'Z'))
			{
				return (0);
			}
		}
		c++;
	}
	return (1);
}
